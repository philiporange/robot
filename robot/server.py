def compute_file_hash(file_path: Path) -> Optional[str]:
    """Compute SHA256 hash of a file."""
    if not file_path.exists():
        return None
    try:
        return hashlib.sha256(file_path.read_bytes()).hexdigest()[:16]
    except Exception:
        return None


def track_modified_files(conv: Conversation, working_dir: Path) -> list[str]:
    """Track which files have been modified since last check."""
    modified = []

    # Get all tracked files for this conversation
    tracked = {f.file_path: f for f in conv.modified_files}

    # Check git status for modifications
    try:
        result = subprocess.run(
            ["git", "diff", "--name-only", "HEAD"],
            cwd=working_dir,
            capture_output=True,
            text=True,
            timeout=10
        )
        if result.returncode == 0:
            for line in result.stdout.strip().split('\n'):
                if line:
                    file_path = str(working_dir / line)
                    if file_path not in tracked:
                        ModifiedFile.create(
                            conversation=conv,
                            file_path=file_path,
                            original_hash=compute_file_hash(Path(file_path))
                        )
                    modified.append(file_path)
    except Exception as e:
        logger.warning(f"Failed to track files: {e}")

    return modified


@app.post("/api/conversations/{conv_id}/messages", response_model=MessageResponse)
async def send_message(
    conv_id: str,
    message: MessageCreate,
    session: Session = Depends(get_session)
):
    """Send a message and get agent response."""
    conv = Conversation.get_or_none(
        (Conversation.id == conv_id) &
        (Conversation.session == session)
    )
    if not conv:
        raise HTTPException(status_code=404, detail="Conversation not found")

    # Store user message
    user_msg = Message.create(
        conversation=conv,
        role="user",
        content=message.content
    )

    # Configure agent
    config = AgentConfig(
        model=conv.model,
        working_dir=Path(conv.working_dir),
        prompt_prefix=WEB_PROMPT_PREFIX,
        session_id=conv.claude_session_id,
        resume=conv.claude_session_id is not None
    )

    # Run agent
    start_time = time.time()
    try:
        agent = Robot.get(conv.agent, config=config)
        response = agent.run(
            prompt=message.content,
            working_dir=Path(conv.working_dir)
        )

        duration_ms = int((time.time() - start_time) * 1000)

        # Track modified files
        files_modified = track_modified_files(conv, Path(conv.working_dir))

        # Update session ID for resume capability
        if hasattr(response, 'session_id') and response.session_id:
            conv.claude_session_id = response.session_id
            conv.save()

        # Store assistant message
        assistant_msg = Message.create(
            conversation=conv,
            role="assistant",
            content=response.content if response.success else f"Error: {response.error}",
            raw_response=response.raw_output,
            files_modified=json.dumps(files_modified) if files_modified else None,
            duration=duration_ms
        )

        # Update conversation timestamp
        conv.updated_at = datetime.now(timezone.utc)
        conv.save()

        return MessageResponse(
            id=assistant_msg.id,
            role="assistant",
            content=assistant_msg.content,
            files_modified=files_modified,
            duration=duration_ms,
            created_at=assistant_msg.created_at
        )

    except Exception as e:
        logger.error(f"Agent error: {e}")

        error_msg = Message.create(
            conversation=conv,
            role="assistant",
            content=f"Error running agent: {str(e)}",
            duration=int((time.time() - start_time) * 1000)
        )

        return MessageResponse(
            id=error_msg.id,
            role="assistant",
            content=error_msg.content,
            files_modified=None,
            duration=error_msg.duration,
            created_at=error_msg.created_at
        )


@app.post("/api/conversations/{conv_id}/messages/stream")
async def send_message_stream(
    conv_id: str,
    message: MessageCreate,
    session: Session = Depends(get_session)
):
    """
    Send a message with streaming status updates via Server-Sent Events.

    Streams status events as the agent works, then sends the final response.
    Event types:
      - status: Real-time status update (tool start, tool complete, etc.)
      - message: Final assistant message
      - error: Error occurred
    """
    conv = Conversation.get_or_none(
        (Conversation.id == conv_id) &
        (Conversation.session == session)
    )
    if not conv:
        raise HTTPException(status_code=404, detail="Conversation not found")

    async def event_generator():
        # Store user message
        user_msg = Message.create(
            conversation=conv,
            role="user",
            content=message.content
        )

        # Queue for status events from agent callback
        status_queue = asyncio.Queue()
        # Collect actions for persistent display
        collected_actions = []

        def on_status(event: StatusEventModel):
            """Callback to receive status events from the agent."""
            # Store status event in database
            StatusEvent.create(
                conversation=conv,
                event_type=event.type.value,
                status_message=event.message,
                tool_name=event.tool_name
            )
            # Convert to action if applicable
            action = status_event_to_action(event)
            if action:
                collected_actions.append(action)
            # Put event in queue for streaming
            try:
                status_queue.put_nowait(event)
            except asyncio.QueueFull:
                pass

        # Configure agent
        config = AgentConfig(
            model=conv.model,
            working_dir=Path(conv.working_dir),
            prompt_prefix=WEB_PROMPT_PREFIX,
            session_id=conv.claude_session_id,
            resume=conv.claude_session_id is not None
        )

        start_time = time.time()

        # Run agent in executor to not block event loop
        loop = asyncio.get_event_loop()

        def run_agent():
            agent = Robot.get(conv.agent, config=config)
            return agent.run(
                prompt=message.content,
                working_dir=Path(conv.working_dir),
                on_status=on_status
            )

        # Start agent task
        agent_task = loop.run_in_executor(None, run_agent)

        # Stream status events while agent runs
        try:
            while not agent_task.done():
                try:
                    # Wait for status events with timeout
                    event = await asyncio.wait_for(
                        status_queue.get(),
                        timeout=0.1
                    )
                    yield {
                        "event": "status",
                        "data": json.dumps({
                            "type": event.type.value,
                            "message": event.message,
                            "tool_name": event.tool_name
                        })
                    }
                except asyncio.TimeoutError:
                    continue

            # Drain remaining events
            while not status_queue.empty():
                event = status_queue.get_nowait()
                yield {
                    "event": "status",
                    "data": json.dumps({
                        "type": event.type.value,
                        "message": event.message,
                        "tool_name": event.tool_name
                    })
                }

            # Get agent response
            response = await agent_task
            duration_ms = int((time.time() - start_time) * 1000)

            # Track modified files
            files_modified = track_modified_files(conv, Path(conv.working_dir))

            # Serialize actions for storage
            actions_json = json.dumps([a.model_dump() for a in collected_actions]) if collected_actions else None

            # Store assistant message
            assistant_msg = Message.create(
                conversation=conv,
                role="assistant",
                content=response.content if response.success else f"Error: {response.error}",
                raw_response=response.raw_output,
                files_modified=json.dumps(files_modified) if files_modified else None,
                actions=actions_json,
                duration=duration_ms
            )

            # Update conversation timestamp
            conv.updated_at = datetime.now(timezone.utc)
            conv.save()

            # Send final message with actions
            yield {
                "event": "message",
                "data": json.dumps({
                    "id": assistant_msg.id,
                    "role": "assistant",
                    "content": assistant_msg.content,
                    "files_modified": files_modified,
                    "actions": [a.model_dump() for a in collected_actions],
                    "duration": duration_ms,
                    "created_at": assistant_msg.created_at.isoformat()
                })
            }

        except Exception as e:
            logger.error(f"Agent error: {e}")
            yield {
                "event": "error",
                "data": json.dumps({"error": str(e)})
            }

    return EventSourceResponse(event_generator())


