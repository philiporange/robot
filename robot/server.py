"""
FastAPI server for Robot web interface.

Provides a REST API for managing conversations with coding agents,
cookie-based authentication via one-time codes, folder browsing,
and real-time chat functionality. Uses SQLite with Peewee for persistence.

Authentication flow:
1. CLI generates a one-time auth code via `robot auth`
2. User visits the link (or scans QR code)
3. Server validates code and sets a long-term browser cookie
4. Cookie is used to create/renew 1-week sessions
"""

