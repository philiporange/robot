# Datetime utilities
def parse_db_datetime(dt) -> datetime:
    """Parse datetime from database (may be string or datetime)."""
    if isinstance(dt, datetime):
        return dt
    if isinstance(dt, str):
        # Handle ISO format with or without timezone
        dt = dt.replace('Z', '+00:00')
        try:
            return datetime.fromisoformat(dt)
        except ValueError:
            # Fallback for different formats
            return datetime.strptime(dt, "%Y-%m-%d %H:%M:%S.%f")
    return dt


# Auth utilities
def create_auth_code() -> AuthCode:
    """Create a new one-time authentication code."""
    code = secrets.token_urlsafe(32)
    expires_at = datetime.now(timezone.utc) + timedelta(hours=AUTH_CODE_EXPIRY_HOURS)
    return AuthCode.create(code=code, expires_at=expires_at)


def verify_session(request: Request) -> Session:
    """Verify session from cookie and return session object."""
    token = request.cookies.get(COOKIE_NAME)
    if not token:
        raise HTTPException(status_code=401, detail="Not authenticated")

    session = Session.get_or_none(
        (Session.token == token) &
        (Session.is_active == True)
    )

    if not session:
        raise HTTPException(status_code=401, detail="Invalid session")

    now = datetime.now(timezone.utc)
    expires_at = parse_db_datetime(session.expires_at)
    last_active = parse_db_datetime(session.last_active)

    # Check if session expired
    if expires_at < now:
        session.is_active = False
        session.save()
        raise HTTPException(status_code=401, detail="Session expired")

    # Renew session if first request in the last hour
    if last_active < now - timedelta(hours=SESSION_RENEWAL_HOURS):
        session.expires_at = now + timedelta(days=SESSION_EXPIRY_DAYS)
        session.last_active = now
        session.save()
    elif last_active < now - timedelta(minutes=1):
        # Update last_active less frequently to reduce DB writes
        session.last_active = now
        session.save()

    return session


def get_session(request: Request) -> Session:
    """Dependency to get current session."""
    return verify_session(request)


# Auth endpoints
@app.get("/api/auth/verify")
async def verify_auth_code(code: str, response: Response):
    """
    Verify a one-time auth code and create a session.

    Returns a redirect to the main page with session cookie set.
    """
    auth_code = AuthCode.get_or_none(
        (AuthCode.code == code) &
        (AuthCode.used == False)
    )

    if not auth_code:
        raise HTTPException(status_code=401, detail="Invalid or expired code")

    now = datetime.now(timezone.utc)
    expires_at = parse_db_datetime(auth_code.expires_at)

    if expires_at < now:
        raise HTTPException(status_code=401, detail="Code expired")

    # Mark code as used
    auth_code.used = True
    auth_code.save()

    # Create new session
    session_token = secrets.token_urlsafe(48)
    session = Session.create(
        token=session_token,
        expires_at=now + timedelta(days=SESSION_EXPIRY_DAYS)
    )

    # Set cookie and redirect to main page
    redirect = RedirectResponse(url="/", status_code=302)
    redirect.set_cookie(
        key=COOKIE_NAME,
        value=session_token,
        max_age=COOKIE_MAX_AGE,
        httponly=True,
        samesite="lax"
    )
    return redirect


@app.get("/api/auth/status")
async def auth_status(request: Request):
    """Check if the current session is valid."""
    try:
        session = verify_session(request)
        return {"authenticated": True, "session_id": session.id}
    except HTTPException:
        return {"authenticated": False}


@app.post("/api/auth/logout")
async def logout(request: Request, response: Response):
    """Logout and invalidate session."""
    token = request.cookies.get(COOKIE_NAME)
    if token:
        session = Session.get_or_none(Session.token == token)
        if session:
            session.is_active = False
            session.save()

    response.delete_cookie(COOKIE_NAME)
    return {"status": "logged out"}


# Folder endpoints
@app.get("/api/folders", response_model=list[FolderItem])
async def list_folders(
    path: Optional[str] = None,
    session: Session = Depends(get_session)
):
    """List folders in a directory."""
    base_path = Path(path) if path else DEFAULT_CODE_DIR

    if not base_path.exists():
        raise HTTPException(status_code=404, detail="Directory not found")

    if not base_path.is_dir():
        raise HTTPException(status_code=400, detail="Path is not a directory")

    items = []
    try:
        for item in sorted(base_path.iterdir()):
            # Skip hidden files and common non-project dirs
            if item.name.startswith('.'):
                continue
            if item.name in ['node_modules', '__pycache__', 'venv', '.venv']:
                continue

            items.append(FolderItem(
                name=item.name,
                path=str(item),
                is_dir=item.is_dir()
            ))
    except PermissionError:
        raise HTTPException(status_code=403, detail="Permission denied")

    return items


@app.post("/api/folders", response_model=FolderItem)
async def create_folder(
    folder: FolderCreate,
    session: Session = Depends(get_session)
):
    """Create a new folder."""
    parent = Path(folder.parent_path)
    if not parent.exists():
        raise HTTPException(status_code=404, detail="Parent directory not found")

    new_folder = parent / folder.name
    if new_folder.exists():
        raise HTTPException(status_code=400, detail="Folder already exists")

    try:
        new_folder.mkdir(parents=True)
        return FolderItem(
            name=folder.name,
            path=str(new_folder),
            is_dir=True
        )
    except PermissionError:
        raise HTTPException(status_code=403, detail="Permission denied")


