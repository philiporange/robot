def status_event_to_action(event: StatusEventModel) -> Optional[ActionItem]:
    """Convert a status event to an ActionItem for display."""
    if event.type != StatusType.TOOL_START or not event.tool_name:
        return None

    tool_name = event.tool_name
    config = ACTION_CONFIGS.get(tool_name, {"icon": "🔧", "color": "action-tool", "type": "tool"})

    # Extract meaningful detail from tool input
    detail = ""
    name = tool_name

    if event.tool_input:
        if tool_name == "Bash":
            cmd = event.tool_input.get("command", "")
            desc = event.tool_input.get("description", "")
            name = desc if desc else (cmd[:40] + "..." if len(cmd) > 40 else cmd)
            detail = cmd
        elif tool_name in ("Read", "Write", "Edit"):
            path = event.tool_input.get("file_path", "")
            name = path.split("/")[-1] if path else tool_name
            detail = path
        elif tool_name == "Glob":
            pattern = event.tool_input.get("pattern", "")
            name = f"glob: {pattern}"
            detail = pattern
        elif tool_name == "Grep":
            pattern = event.tool_input.get("pattern", "")
            name = f"grep: {pattern[:30]}" if len(pattern) > 30 else f"grep: {pattern}"
            detail = pattern
        elif tool_name == "Task":
            desc = event.tool_input.get("description", "")
            name = desc if desc else "Subagent"
            detail = event.tool_input.get("prompt", "")[:100]
        else:
            name = event.message[:40] if event.message else tool_name
            detail = str(event.tool_input)[:200]

    return ActionItem(
        type=config["type"],
        name=name,
        detail=detail,
        icon=config["icon"],
        color=config["color"]
    )


